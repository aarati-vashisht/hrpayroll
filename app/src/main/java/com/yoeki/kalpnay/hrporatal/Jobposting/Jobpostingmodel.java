package com.yoeki.kalpnay.hrporatal.Jobposting;

public class Jobpostingmodel {

    String nocadicdates;
    String opening;
    String hiringlead;
    String createdon;
    String status;

    public String getNocadicdates() {
        return nocadicdates;
    }

    public void setNocadicdates(String nocadicdates) {
        this.nocadicdates = nocadicdates;
    }

    public String getOpening() {
        return opening;
    }

    public void setOpening(String opening) {
        this.opening = opening;
    }

    public String getHiringlead() {
        return hiringlead;
    }

    public void setHiringlead(String hiringlead) {
        this.hiringlead = hiringlead;
    }

    public String getCreatedon() {
        return createdon;
    }

    public void setCreatedon(String createdon) {
        this.createdon = createdon;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
